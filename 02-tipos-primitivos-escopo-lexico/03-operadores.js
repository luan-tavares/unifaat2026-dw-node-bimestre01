// 03-operadores.js

// BLOCO 1: matemáticos
console.log(10 + 2); // 12
console.log(10 - 2); // 8
console.log(10 * 2); // 20
console.log(10 / 2); // 5
console.log(10 % 3); // 1

// BLOCO 2: lógicos
console.log(true && false); // false
console.log(true || false); // true
console.log(!true);         // false

// BLOCO 3: comparação (== vs ===)
console.log(2 == "2");   // true
console.log(2 === "2");  // false
console.log(2 !== "2");  // true
console.log(2 > 1);      // true
console.log(2 >= 2);     // true
console.log(2 < 1);      // false

// BLOCO 4: concatenação e conversão dinâmica
console.log("1" + 2);      // 12
console.log(2 + "2");      // 22
console.log(2 + (+"2"));   // 4